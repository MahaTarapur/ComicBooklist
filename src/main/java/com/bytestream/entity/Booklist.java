package com.bytestream.entity;

public class Booklist 

{
	private long year;
	private long price;
	private String name;
	private String author;
	public long getYear() {
		return year;
	}
	public long getPrice() {
		return price;
	}
	public String getName() {
		return name;
	}
	public String getAuthor() {
		return author;
	}
	public Booklist(long year, long price, String name, String author) {
		super();
		this.year = year;
		this.price = price;
		this.name = name;
		this.author = author;
	}
	
	
	
	
	}
	
	
	
	
	
	

